# Write some code that iterates through and prints all the words, one per line.
vocabulary = [
    ['happy', 'cheerful', 'merry', 'glad'],
    ['tired', 'sleepy', 'fatigued', 'drained'],
    ['excited', 'eager', 'enthused', 'animated'],
]

for element in vocabulary:
    for word in element:
        print(word)

# happy
# cheerful
# merry
# glad
# tired
# sleepy
# etc...
